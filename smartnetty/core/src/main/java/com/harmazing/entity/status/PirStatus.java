package com.harmazing.entity.status;

public class PirStatus {
	private String id;
	private String mac;
	private int alarmed;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMac() {
		return mac;
	}
	public void setMac(String mac) {
		this.mac = mac;
	}
	public int getAlarmed() {
		return alarmed;
	}
	public void setAlarmed(int alarmed) {
		this.alarmed = alarmed;
	}
	
	
}
